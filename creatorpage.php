      <h1 class="Paragrap_title">Markus Persson</h1>
      <p class="Paragraph_text">Persson's meest populaire creatie is de survival sandbox-game Minecraft, die voor het eerst publiekelijk beschikbaar was op 17 mei 2009 en volledig werd uitgebracht op 18 november 2011. Persson verliet zijn baan als game-ontwikkelaar om fulltime aan Minecraft te werken totdat hij klaar was. Begin 2011 verkocht Mojang AB het miljoenste exemplaar van de game, enkele maanden later hun tweede en nog een aantal hun derde. Mojang nam verschillende nieuwe personeelsleden aan voor het Minecraft-team, terwijl Persson de rol van hoofdontwikkelaar aan Jens Bergensten overdroeg. Hij stopte met werken aan Minecraft na een deal met Microsoft om Mojang te verkopen voor $ 2,5 miljard. Dit bracht zijn nettowaarde op 1,5 miljard dollar.</p>
      <br>
      <h1 class="Paragrap_title">een slimme zet!</h1>
      <p class="Paragraph_text">Persson en Jakob Porsér kwamen op het idee voor Scrolls met elementen uit bordspellen en verzamelkaartspellen. Persson merkte op dat hij niet actief betrokken zal zijn bij de ontwikkeling van het spel en dat Porsér het zal ontwikkelen. Persson onthulde op zijn tumblr-blog op 5 augustus 2011 dat hij werd aangeklaagd door een Zweeds advocatenkantoor dat Bethesda Softworks vertegenwoordigde vanwege de handelsmerknaam Scrolls en beweerde dat het in strijd was met hun The Elder Scrolls-gameserie. Op 17 augustus 2011 daagde Persson Bethesda uit voor een Quake 3-toernooi om de uitkomst van het geschil over de naamgeving te beslissen. Op 27 september 2011 bevestigde Persson dat de rechtszaak voor de rechter zou komen. ZeniMax Media, eigenaar van Bethesda Softworks, kondigde de schikking van de rechtszaak in maart 2012 aan.  Door de schikking kon Mojang het handelsmerk Scrolls blijven gebruiken.

      </p>
      <br>
      <h1 class="Paragrap_title">Social media</h1>
      <p class="Paragraph_text">Persson heeft kritiek gekregen vanwege de politieke en sociale meningen die hij op Twitter uitsprak, zoals het noemen van feminisme als een 'sociale ziekte' en beweren dat de meeste feministen 'openlijk seksistisch zijn tegen mannen'. In juni 2017 kreeg Persson te maken met kritiek omdat ze videogame-ontwikkelaar Zoë Quinn een ‘kut’ noemt. Later in juni 2017 tweette hij ter ondersteuning van een heteroseksuele pride-dag, waarbij hij tegenstanders opriep voor het idee "kut" en suggereerde dat ze "het verdienen om neergeschoten te worden". Nadat hij te maken kreeg met terugslag van de gemeenschap, verwijderde hij de tweets en liep hij zijn uitspraken terug, terwijl hij in één tweet schreef: "Dus ja, het gaat om trots om te durven uiten, niet om trots te zijn wie je bent. Ik snap het nu”.

      In november 2017 werd Persson bekritiseerd voor het plaatsen van een tweet met de tekst: "Het is oké om blank te zijn". In vervolg-tweets zei hij dat hij geloofde dat privilege een "verzonnen maatstaf" is. In maart 2019 kreeg hij kritiek omdat hij transgendervrouwen geestelijk ziek noemde. Deze controverses leidden tot de creatie van de internetmeme "Hatsune Miku created Minecraft", die opkwam als gevolg van een virale tweet en werd gemaakt door fans van het spel om steun te betuigen aan Minecraft terwijl ze afstand namen van Persson.

      Een Minecraft-update van maart 2019 verwijderde stilletjes verwijzingen naar Persson uit het menu van de game, hoewel zijn naam nog steeds in de aftiteling staat. Microsoft heeft de exacte redenen niet gespecificeerd, maar de timing van de verwijdering leidde ertoe dat meerdere nieuwsuitzendingen concluderen dat het verband hield met de controversiële tweets. Persson was later dat jaar niet uitgenodigd om deel te nemen aan het tienjarig jubileumevenement van Minecraft, waarbij Microsoft zei dat zijn opvattingen "niet die van Microsoft of Mojang weerspiegelen". Microsoft nam afstand van Persson vanwege zijn controversiële opmerkingen op zijn Twitter-account.
</p>
